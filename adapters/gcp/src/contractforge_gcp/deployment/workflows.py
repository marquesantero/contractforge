def _cleanup_queries(
    evidence_prefix: str,
    targets: tuple[GCPWorkflowReadbackTarget, ...],
    target_tables: tuple[str, ...],
    contract_names: tuple[str, ...],
) -> dict[str, str]:
    queries: dict[str, str] = {}
    seen_targets: set[str] = set()
    for target in targets:
        if not target.target_table or target.target_table in seen_targets:
            continue
        seen_targets.add(target.target_table)
        queries[f"01_drop_target_{_cleanup_query_key(target.step_name)}"] = (
            f"DROP TABLE IF EXISTS `{target.target_table}`;"
        )
    if target_tables:
        target_filter = _sql_in_list(target_tables)
        queries["02_delete_run_evidence_by_target"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_run_evidence` WHERE target_table IN ({target_filter});"
        )
        queries["03_delete_schema_evidence_by_target"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_schema_evidence` WHERE target_table IN ({target_filter});"
        )
        queries["04_delete_annotation_evidence_by_target"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_annotation_evidence` WHERE target_table IN ({target_filter});"
        )
        queries["05_delete_governance_evidence_by_target"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_governance_evidence` WHERE target_table IN ({target_filter});"
        )
        queries["06_delete_lineage_evidence_by_target"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_lineage_evidence` WHERE target_table IN ({target_filter});"
        )
    if contract_names:
        contract_filter = _sql_in_list(contract_names)
        queries["07_delete_quality_evidence_by_contract"] = (
            f"DELETE FROM `{evidence_prefix}.contractforge_quality_evidence` WHERE contract_name IN ({contract_filter});"
        )
    return queries


def _cleanup_query_key(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9]+", "_", value).strip("_").lower() or "target"


def _sql_in_list(values: tuple[str, ...]) -> str:
    if not values:
        return "''"
    return ", ".join(_sql_string(value) for value in values)


def _sql_string(value: str) -> str:
    return "'" + value.replace("\\", "\\\\").replace("'", "\\'") + "'"


