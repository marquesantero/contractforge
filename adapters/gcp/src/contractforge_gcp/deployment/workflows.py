def render_gcp_workflows_runner_yaml(
    *,
    project_name: str,
    project_id: str,
    operations: tuple[GCPWorkflowOperation, ...],
    poll_max_attempts: int = 30,
    poll_sleep_seconds: int = 10,
    connector_retry_max_retries: int = 5,
    connector_retry_initial_delay_seconds: int = 2,
    connector_retry_max_delay_seconds: int = 60,
    connector_retry_multiplier: int = 2,
) -> str:
    """Render a deterministic Workflows source plan for ordered BigQuery jobs."""

    workflow_steps: list[dict[str, Any]] = [
        {
            "init": {
                "assign": [
                    {"contractforge_project": project_name},
                    {"gcp_project_id": project_id},
                    {"contractforge_run_id": '${"workflows:" + sys.get_env("GOOGLE_CLOUD_WORKFLOW_EXECUTION_ID")}'},
                    {"runner_status": "PLANNED_REVIEW_REQUIRED"},
                    {"poll_max_attempts": poll_max_attempts},
                    {"poll_sleep_seconds": poll_sleep_seconds},
                    {"connector_retry_max_retries": connector_retry_max_retries},
                    {"connector_retry_initial_delay_seconds": connector_retry_initial_delay_seconds},
                    {"connector_retry_max_delay_seconds": connector_retry_max_delay_seconds},
                    {"connector_retry_multiplier": connector_retry_multiplier},
                ]
            }
        }
    ]
    for index, operation in enumerate(operations, start=1):
        workflow_steps.extend(_workflow_steps(index, project_id, operation))
    workflow_steps.append(
        {
            "done": {
                "return": {
                    "status": "CERTIFIED_FOR_STABLE_SURFACE",
                    "project": project_name,
                    "operation_count": len(operations),
                    "wait_polling_included": True,
                    "review_boundary": (
                        "Generated by ContractForge for the certified Workflows deployment, "
                        "execution and evidence readback command path."
                    ),
                }
            }
        }
    )
    return yaml.dump(
        {"main": {"steps": workflow_steps}},
        Dumper=_WorkflowYamlDumper,
        sort_keys=False,
        allow_unicode=False,
        width=4096,
    )


