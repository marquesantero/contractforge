def workflow_name(project_name: str) -> str:
    value = _slug(f"cf-{project_name}-runner")
    return value[:63].rstrip("-") or "cf-project-runner"


def _workflow_steps(index: int, project_id: str, operation: GCPWorkflowOperation) -> list[dict[str, Any]]:
    step_id = _step_id(index, operation)
    if operation.operation == "QUERY":
        failed_run_steps = _failed_run_evidence_steps(index, step_id, project_id, operation)
        return [
            _query_or_load_job_step(
                step_id,
                project_id,
                {"query": {"query": operation.body, "useLegacySql": False}},
                failed_run_steps=failed_run_steps,
            ),
            *_polling_steps(
                step_id,
                project_id,
                on_error_steps=failed_run_steps,
            ),
            *_evidence_steps(index, step_id, project_id, operation),
        ]
    if operation.operation == "LOAD":
        failed_run_steps = _failed_run_evidence_steps(index, step_id, project_id, operation)
        return [
            _query_or_load_job_step(
                step_id,
                project_id,
                {"load": _load_configuration(operation.body, project_id)},
                failed_run_steps=failed_run_steps,
            ),
            *_polling_steps(
                step_id,
                project_id,
                on_error_steps=failed_run_steps,
            ),
            *_evidence_steps(index, step_id, project_id, operation),
        ]
    if operation.operation == "SCHEMA_EVIDENCE":
        sql = _schema_evidence_insert_sql(project_id, operation, run_id_literal="@run_id")
        return [
            _query_job_step(
                step_id,
                project_id,
                sql,
                query_parameters=[_string_query_parameter("run_id", _workflow_evidence_run_id_expr(operation))],
            ),
            *_polling_steps(step_id, project_id),
        ]
    return [
        {
            step_id: {
                "assign": [
                    {
                        f"{step_id}_review_required": (
                            f"{operation.operation} artifact {operation.artifact} requires adapter-specific review."
                        )
                    }
                ]
            }
        }
    ]


def _evidence_steps(
    index: int,
    step_id: str,
    project_id: str,
    operation: GCPWorkflowOperation,
) -> list[dict[str, Any]]:
    if operation.operation_name in {"write_target", "load_source"}:
        sql = _run_evidence_insert_sql(project_id, operation, run_id_literal="@run_id")
        if sql:
            evidence_operation = GCPWorkflowOperation(
                step_name=operation.step_name,
                operation_name=f"{operation.operation_name}_run_evidence",
                operation="QUERY",
                artifact=f"{operation.artifact}#run_evidence",
                body=sql,
            )
            evidence_step_id = _step_id(index, evidence_operation)
            return [
                _query_job_step(
                    evidence_step_id,
                    project_id,
                    sql,
                    query_parameters=[
                        _string_query_parameter("run_id", _workflow_evidence_run_id_expr(operation))
                    ],
                ),
                *_polling_steps(evidence_step_id, project_id),
            ]
    if operation.operation_name == "quality":
        return _quality_evidence_steps(index, step_id, project_id, operation)
    return []


def _query_or_load_job_step(
    step_id: str,
    project_id: str,
    configuration: dict[str, Any],
    *,
    failed_run_steps: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    payload: dict[str, Any] = {
        "try": {
            "call": "googleapis.bigquery.v2.jobs.insert",
            "args": {
                "projectId": project_id,
                "body": {"configuration": configuration},
            },
            "result": f"{step_id}_job",
        },
        "retry": _retry_policy(predicate="http.default_retry_predicate_non_idempotent"),
    }
    if failed_run_steps:
        payload["except"] = {
            "as": "e",
            "steps": _failed_run_exception_steps(step_id, failed_run_steps),
        }
    return {step_id: payload}


def _failed_run_evidence_steps(
    index: int,
    step_id: str,
    project_id: str,
    operation: GCPWorkflowOperation,
) -> list[dict[str, Any]] | None:
    if operation.operation_name not in {"write_target", "load_source"}:
        return None
    sql = _run_evidence_insert_sql(
        project_id,
        operation,
        status="FAILED",
        run_id_literal="@run_id",
        error_message_literal=_sql_string("BigQuery Job failed."),
    )
    if not sql:
        return None
    failed_operation = GCPWorkflowOperation(
        step_name=operation.step_name,
        operation_name=f"{operation.operation_name}_failed_run_evidence",
        operation="QUERY",
        artifact=f"{operation.artifact}#failed_run_evidence",
        body=sql,
    )
    failed_step_id = _step_id(index, failed_operation)
    failed_raise_step = _step_id_with_suffix(step_id, "failed_raise")
    return [
        _query_job_step(
            failed_step_id,
            project_id,
            sql,
            query_parameters=[
                _string_query_parameter("run_id", _workflow_evidence_run_id_expr(operation)),
            ],
        ),
        *_polling_steps(failed_step_id, project_id),
        {failed_raise_step: {"raise": "BigQuery Job failed."}},
    ]


def _failed_run_exception_steps(step_id: str, failed_run_steps: list[dict[str, Any]]) -> list[dict[str, Any]]:
    error_var = f"{step_id}_error_message"
    assign_step = _step_id_with_suffix(step_id, "insert_error")
    return [
        {
            assign_step: {
                "assign": [{error_var: "BigQuery Job failed."}],
                "next": next(iter(failed_run_steps[0])),
            }
        }
    ]


def _query_job_step(
    step_id: str,
    project_id: str,
    sql: str,
    *,
    query_parameters: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    query_config: dict[str, Any] = {"query": sql, "useLegacySql": False}
    if query_parameters:
        query_config["parameterMode"] = "NAMED"
        query_config["queryParameters"] = query_parameters
    return {
        step_id: {
            "try": {
                "call": "googleapis.bigquery.v2.jobs.insert",
                "args": {
                    "projectId": project_id,
                    "body": {"configuration": {"query": query_config}},
                },
                "result": f"{step_id}_job",
            },
            "retry": _retry_policy(predicate="http.default_retry_predicate_non_idempotent"),
        }
    }


def _quality_evidence_steps(
    index: int,
    step_id: str,
    project_id: str,
    operation: GCPWorkflowOperation,
) -> list[dict[str, Any]]:
    if not operation.evidence_dataset or not operation.contract_name:
        return []
    results_step = _step_id_with_suffix(step_id, "results")
    assign_step = _step_id_with_suffix(step_id, "failed_rows")
    check_step = _step_id_with_suffix(step_id, "check")
    failed_raise_step = _step_id_with_suffix(step_id, "failed_raise")
    results_var = f"{step_id}_results"
    failed_rows_var = f"{step_id}_failed_rows"
    failed_operation = GCPWorkflowOperation(
        step_name=operation.step_name,
        operation_name="quality_failed_evidence",
        operation="QUERY",
        artifact=f"{operation.artifact}#quality_failed_evidence",
        body="",
    )
    passed_operation = GCPWorkflowOperation(
        step_name=operation.step_name,
        operation_name="quality_passed_evidence",
        operation="QUERY",
        artifact=f"{operation.artifact}#quality_passed_evidence",
        body="",
    )
    failed_step_id = _step_id(index, failed_operation)
    passed_step_id = _step_id(index, passed_operation)
    failed_sql = _quality_evidence_insert_sql(
        project_id,
        operation,
        status="FAILED",
        run_id_literal="@run_id",
        failed_rows_literal="@failed_rows",
    )
    passed_sql = _quality_evidence_insert_sql(
        project_id,
        operation,
        status="PASSED",
        run_id_literal="@run_id",
        failed_rows_literal="0",
    )
    failed_query_parameters = [
        _string_query_parameter("run_id", _workflow_evidence_run_id_expr(operation)),
        {
            "name": "failed_rows",
            "parameterType": {"type": "INT64"},
            "parameterValue": {"value": f"${{string({failed_rows_var})}}"},
        }
    ]
    passed_query_parameters = [_string_query_parameter("run_id", _workflow_evidence_run_id_expr(operation))]
    return [
        {
            results_step: {
                "call": "googleapis.bigquery.v2.jobs.getQueryResults",
                "args": {
                    "projectId": project_id,
                    "jobId": f"${{{step_id}_job.jobReference.jobId}}",
                    "location": f"${{{step_id}_job.jobReference.location}}",
                },
                "result": results_var,
            }
        },
        {assign_step: {"assign": [{failed_rows_var: f"${{int({results_var}.rows[0].f[0].v)}}"}]}},
        {
            check_step: {
                "switch": [{"condition": f"${{{failed_rows_var} > 0}}", "next": failed_step_id}],
                "next": passed_step_id,
            }
        },
        _query_job_step(failed_step_id, project_id, failed_sql, query_parameters=failed_query_parameters),
        *_polling_steps(failed_step_id, project_id),
        {failed_raise_step: {"raise": f'${{"Quality check failed with " + string({failed_rows_var}) + " failed rows."}}'}},
        _query_job_step(passed_step_id, project_id, passed_sql, query_parameters=passed_query_parameters),
        *_polling_steps(passed_step_id, project_id),
    ]


def _polling_steps(
    step_id: str,
    project_id: str,
    *,
    on_error_steps: list[dict[str, Any]] | None = None,
) -> list[dict[str, Any]]:
    poll_init = _step_id_with_suffix(step_id, "poll_init")
    poll_get = _step_id_with_suffix(step_id, "poll_get")
    poll_check = _step_id_with_suffix(step_id, "poll_check")
    poll_error = _step_id_with_suffix(step_id, "poll_error")
    poll_sleep = _step_id_with_suffix(step_id, "poll_sleep")
    poll_timeout = _step_id_with_suffix(step_id, "poll_timeout")
    poll_done = _step_id_with_suffix(step_id, "poll_done")
    attempt_var = f"{step_id}_attempt"
    status_var = f"{step_id}_status"
    error_var = f"{step_id}_error_message"
    if on_error_steps:
        poll_error_steps = [
            {
                poll_error: {
                    "assign": [{error_var: "BigQuery Job failed."}],
                    "next": next(iter(on_error_steps[0])),
                }
            },
            *on_error_steps,
        ]
    else:
        poll_error_steps = [{poll_error: {"raise": f"${{{status_var}.status.errorResult.message}}"}}]
    return [
        {poll_init: {"assign": [{attempt_var: 0}]}},
        {
            poll_get: {
                "try": {
                    "call": "googleapis.bigquery.v2.jobs.get",
                    "args": {
                        "projectId": project_id,
                        "jobId": f"${{{step_id}_job.jobReference.jobId}}",
                        "location": f"${{{step_id}_job.jobReference.location}}",
                    },
                    "result": status_var,
                },
                "retry": _retry_policy(predicate="http.default_retry_predicate"),
            }
        },
        {
            poll_check: {
                "switch": [
                    {
                        "condition": f'${{{status_var}.status.state == "DONE" and "errorResult" in {status_var}.status}}',
                        "next": poll_error,
                    },
                    {
                        "condition": f'${{{status_var}.status.state == "DONE"}}',
                        "next": poll_done,
                    },
                    {
                        "condition": f"${{{attempt_var} >= poll_max_attempts}}",
                        "next": poll_timeout,
                    },
                ],
                "next": poll_sleep,
            }
        },
        *poll_error_steps,
        {poll_timeout: {"raise": f"BigQuery job {step_id} did not finish before poll_max_attempts."}},
        {
            poll_sleep: {
                "assign": [{attempt_var: f"${{{attempt_var} + 1}}"}],
                "next": _step_id_with_suffix(step_id, "sleep_call"),
            }
        },
        {
            _step_id_with_suffix(step_id, "sleep_call"): {
                "call": "sys.sleep",
                "args": {"seconds": "${poll_sleep_seconds}"},
                "next": poll_get,
            }
        },
        {poll_done: {"assign": [{f"{step_id}_final_state": f"${{{status_var}.status.state}}"}]}},
    ]


def _retry_policy(*, predicate: str) -> dict[str, Any]:
    return {
        "predicate": f"${{{predicate}}}",
        "max_retries": "${connector_retry_max_retries}",
        "backoff": {
            "initial_delay": "${connector_retry_initial_delay_seconds}",
            "max_delay": "${connector_retry_max_delay_seconds}",
            "multiplier": "${connector_retry_multiplier}",
        },
    }


def _target_row_counts_query(targets: tuple[GCPWorkflowReadbackTarget, ...]) -> str:
    selects = [
        (
            f"SELECT {_sql_string(target.step_name)} AS step_name, "
            f"{_sql_string(target.target_table)} AS target_table, COUNT(*) AS row_count "
            f"FROM `{target.target_table}`"
        )
        for target in targets
        if target.target_table
    ]
    if not selects:
        return "SELECT 'NO_TARGETS' AS step_name, 'NO_TARGETS' AS target_table, 0 AS row_count;"
    return "\nUNION ALL\n".join(selects) + "\nORDER BY step_name;"


def _run_evidence_query(
    evidence_prefix: str,
    target_tables: tuple[str, ...],
    *,
    execution_id_placeholder: str | None = None,
) -> str:
    filters = [f"target_table IN ({_sql_in_list(target_tables)})"]
    if execution_id_placeholder:
        filters.append(f"run_id LIKE CONCAT('workflows:', {execution_id_placeholder}, ':%')")
    return "\n".join(
        [
            "SELECT",
            "  target_table,",
            "  COUNT(*) AS run_rows,",
            "  COUNTIF(status = 'SUCCEEDED') AS succeeded_rows,",
            "  COUNTIF(status = 'FAILED') AS failed_rows,",
            "  MAX(finished_at) AS last_finished_at",
            f"FROM `{evidence_prefix}.contractforge_run_evidence`",
            "WHERE " + "\n  AND ".join(filters),
            "GROUP BY target_table",
            "ORDER BY target_table;",
        ]
    )


