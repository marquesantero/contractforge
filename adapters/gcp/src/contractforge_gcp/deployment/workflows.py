class _WorkflowYamlDumper(yaml.SafeDumper):
    pass


def _represent_workflow_string(dumper: yaml.SafeDumper, data: str) -> yaml.nodes.ScalarNode:
    style = "|" if "\n" in data else None
    return dumper.represent_scalar("tag:yaml.org,2002:str", data, style=style)


_WorkflowYamlDumper.add_representer(str, _represent_workflow_string)


@dataclass(frozen=True)
class GCPWorkflowOperation:
    step_name: str
    operation_name: str
    operation: str
    artifact: str
    body: str
    contract_name: str | None = None
    target_table: str | None = None
    evidence_dataset: str | None = None


