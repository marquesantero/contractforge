def _load_configuration(body: str, project_id: str) -> dict[str, Any]:
    payload = json.loads(body)
    destination = _destination_table(str(payload["destination_table"]), project_id)
    config: dict[str, Any] = {
        "sourceUris": list(payload["source_uris"]),
        "destinationTable": destination,
        "sourceFormat": payload["source_format"],
        "writeDisposition": payload["write_disposition"],
    }
    if "autodetect" in payload:
        config["autodetect"] = bool(payload["autodetect"])
    if "skip_leading_rows" in payload:
        config["skipLeadingRows"] = int(payload["skip_leading_rows"])
    if "schema_fields" in payload:
        config["schema"] = {"fields": list(payload["schema_fields"])}
    return config


def _destination_table(ref: str, default_project_id: str) -> dict[str, str]:
    parts = ref.split(".")
    if len(parts) == 3:
        project_id, dataset_id, table_id = parts
    elif len(parts) == 2:
        project_id = default_project_id
        dataset_id, table_id = parts
    else:
        raise ValueError(f"BigQuery destination table must be dataset.table or project.dataset.table: {ref}")
    return {"projectId": project_id, "datasetId": dataset_id, "tableId": table_id}


def _step_id(index: int, operation: GCPWorkflowOperation) -> str:
    return _identifier(f"{index:03d}_{operation.step_name}_{operation.operation_name}")


def _step_id_with_suffix(step_id: str, suffix: str) -> str:
    candidate = f"{step_id}_{suffix}"
    if len(candidate) <= 63:
        return _identifier(candidate)
    digest = hashlib.sha1(candidate.encode("utf-8")).hexdigest()[:8]
    max_base = 63 - len(suffix) - len(digest) - 2
    base = step_id[:max_base].rstrip("_")
    return _identifier(f"{base}_{digest}_{suffix}")


def _identifier(value: str) -> str:
    normalized = re.sub(r"[^A-Za-z0-9_]+", "_", value)
    normalized = re.sub(r"_+", "_", normalized).strip("_")
    if not normalized or not normalized[0].isalpha():
        normalized = f"step_{normalized}"
    if len(normalized) <= 63:
        return normalized
    digest = hashlib.sha1(normalized.encode("utf-8")).hexdigest()[:8]
    base = normalized[: 63 - len(digest) - 1].rstrip("_")
    return f"{base}_{digest}"


def _slug(value: str) -> str:
    lowered = value.lower().replace("_", "-")
    cleaned = re.sub(r"[^a-z0-9-]+", "-", lowered)
    cleaned = re.sub(r"-+", "-", cleaned).strip("-")
    if not cleaned or not cleaned[0].isalpha():
        cleaned = f"cf-{cleaned}"
    return cleaned
